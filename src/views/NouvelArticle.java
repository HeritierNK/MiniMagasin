    private void messageArticleEnregristrement(int res) {
        if(res != 0) {
            this.txtCode.setText(null);
            this.txtDesignation.setText(null);
            this.txtPrix.setText(null);
            
            this.afficherMessage("Article enregistré!");
        } else {
            this.afficherMessage("Données erronées!");
        }
    }
    
    private void afficherMessage(String message) {
        new Thread(new Runnable() {

            @Override
            public void run() {
                lblMessage.setText(message);
                for (int i = 0; i < 3; i++) {
                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        ex.printStackTrace();
                    }
                }
                lblMessage.setText(null);
            }
        }).start();
    }
