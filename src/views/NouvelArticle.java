    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        if(!this.txtCode.getText().isEmpty() && !this.txtDesignation.getText().isEmpty() &&
                !this.txtPrix.getText().isEmpty()) {
            try {
                Article article = null;
                if(this.cbEnSolde.isSelected() && !this.txtRemise.getText().isEmpty()) {
                    article = new ArticleEnSolde(Double.valueOf(this.txtRemise.getText()), this.txtCode.getText(),
                        this.txtDesignation.getText(),
                        Double.valueOf(this.txtPrix.getText()), Categorie.valueOf(this.cmbCategorie.getSelectedItem().toString()));
                } else {
                    article = new Article(this.txtCode.getText(),
                        this.txtDesignation.getText(),
                        Double.valueOf(this.txtPrix.getText()), Categorie.valueOf(this.cmbCategorie.getSelectedItem().toString()));
                }
                
                int res = article.enregistrer();

                this.messageArticleEnregristrement(res);

            } catch (CategorieInvalideException ex) {
                ex.printStackTrace();
            }
        } else {
            this.afficherMessage("Données incomplètes!");
        }        
    }//GEN-LAST:event_jButton1ActionPerformed

